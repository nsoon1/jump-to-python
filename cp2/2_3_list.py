#리스트 인덱싱과 슬라이싱
#리스트 인덱싱 - 리스트 내에 저장된 값에 접근하는 방법, 인덱스는 0부터 시작
# a = [1,2,3]
# print(a[0]) # 1
# print(a[0] + a[2]) # 1 + 3 = 4
# print(a[-1]) # 3
# b = [1,2,3, ['a','b','c']]
# print(b[3]) # ['a','b','c'], == b[-1]
# print(b[3][0]) # 'a'
#리스트 슬라이싱 - 리스트 내에서 특정 구간에 속하는 값들을 가져오는 방법
# a = [1,2,3,4,5]
# print(a[0:2]) # [1,2], 인덱스 0부터 2 직전까지
# print(a[2:]) # [3,4,5], 인덱스 2부터 끝까지
# print(a[:2]) # [1,2], 인덱스 처음부터 2 직전까지
# a =[1,2,3,4,5] 리스트에서 슬라이싱 기법을 사용하여 리스트 [2,3]을 출력해보자
# a = [1,2,3,4,5]
# print(a[1:3])
# print(a[-4:-2])
#리스트 연산하기
#리스트 더하기(+) - 두 개 이상의 리스트를 연결해 새로운 리스트를 만듦
# a = [1,2,3]
# b = [4,5,6]
# print(a + b) # [1,2,3,4,5,6]
#리스트 반복하기(*) - 리스트를 여러 번 반복해 새로운 리스트를 만듦
# a = [1,2,3]
# print(a * 3) # [1,2,3,1,2,3,1,2,3]
#리스트에서 +와 * 연산자를 사용해 [1,2,3,1,2,3,1,2,3] 리스트를 만들어보자
# a = [1,2]
# b = [3]
# print((a + b) * 3)
