# 리스트 수정과 삭제
#리스트 수정 - 인덱싱을 사용해 리스트 내의 특정 값을 변경
# a = [1, 2, 3]
# a[2] = 4
# print(a) # [1, 2, 4]
#리스트 삭제 - del 함수를 사용해 리스트 내의 특정 값을 삭제
# a = [1,2,3]
# del a[1]
# print(a) # [1,3]
# a = [1,2,3,4,5]
# del a[2:] # 인덱스 2부터 끝까지 삭제
# print(a) # [1,2]
#리스트 관련 함수
#1. append 함수 - 리스트의 마지막에 새로운 값을 추가하는 함수
# a = [1,2,3]
# a.append(4)
# print(a) # [1,2,3,4]
# a.append([5,6]) # 리스트에 리스트 추가 가능
# print(a) # [1,2,3,4,[5,6]]
#2. sort 함수 - 리스트 내의 값들을 오름차순으로 정렬하는 함수
# a = [3,1,2]
# a.sort()
# print(a) # [1,2,3]
# b = ['b','c','a']
# b.sort()
# print(b) # ['a','b','c']
#3. reverse 함수 - 리스트 내의 값들의 순서를 반대로 뒤집는 함수
# a = [1,2,3]
# a.reverse()
# print(a) # [3,2,1]
#4. index 함수 - 리스트 내에 특정 값이 존재하는지 확인하고, 존재한다면 그 위치(인덱스)를 알려주는 함수
# a = [1,2,3]
# print(a.index(3)) # 2
# print(a.index(1)) # 0
#5. insert 함수 - 리스트의 특정 위치(인덱스)에 새로운 값을 삽입하는 함수
# a = [1,2,3]
# a.insert(0,4) # 인덱스 0 위치에 4 삽입
# print(a) # [4,1,2,3]
#6. remove 함수 - 리스트 내에 특정 값을 찾아서 삭제하는 함수
# a = [1,2,3,1,2,3]
# a.remove(3) # 첫 번째로 나오는 3 삭제
# print(a) # [1,2,1,2,3]
#7. pop 함수 - 리스트의 맨 마지막에 있는 값을 꺼내는 함수, 꺼낸 값은 리스트에서 삭제됨
# a = [1,2,3]
# print(a.pop()) # 3
# print(a) # [1,2]
# pop(x) 형태로 사용하면 인덱스 x 위치에 있는 값을 꺼냄
# a = [1,2,3]
# print(a.pop(1)) # 2
# print(a) # [1,3]
#8. count 함수 - 리스트 내에 특정 값이 몇 개 있는지 세는 함수
# a = [1,2,3,1]
# print(a.count(1)) # 2
#9. extend 함수 - 리스트에 새로운 리스트를 더하는 함수
# a = [1,2,3]
# a.extend([4,5]) # a 리스트에 [4,5] 리스트를 더함
# print(a) # [1,2,3,4,5]
# a.extend([4,5])는 a += [4,5]와 동일한 표현