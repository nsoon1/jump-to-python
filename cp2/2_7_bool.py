#2-7 불 자료형
#불 자료형 - 참(True)과 거짓(False)을 나타내는 자료형
#불 자료형은 True, False 두가지의 값만 가질 수 있음
# a = True
# b = False
# print(type(a)) #<class 'bool'>
# print(type(b)) #<class 'bool'>
# print(1 == 1) # True
# print(2 > 1) # True
# print(2 < 1) # False
#자료형의 참과 거짓
#"python" = True "" = False [1,2,3] = True [] = False (1,2,3) = True () = False {'a': 1} = True {} = False 1 = True 0 = False None = False
#불 연산 - 자료형의 참과 거짓을 식별하는 방법
#print(bool('python')) #True
#print(bool('')) #False